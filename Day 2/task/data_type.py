# Task:

# Subscripting
print("Hello"[0])
print("Hello"[-1])
# output: "o" ---> negative value represent accessing data
# from last position of the string character

# Concatenation
print("123" + "234") # output: "123234"

# Integer ===> Whole Number
print(123 + 123) # output: 246

# Larger Integer
# if we need larger number lai 123,456,789 replace comma with integer python
# will automatically interpret it.
print(123_456_789)

# Float ===> Floating Point number (Example: 3.14159)
print(3.14159)

# Boolean
print(True)
print(False)
